#!/usr/bin/env python3
from __future__ import annotations

import argparse
import csv
import json
from pathlib import Path
from typing import Any


BENCH_PREFIX_TO_GROUP = {
    "stage-bench": "stage",
    "micro-bench": "micro",
    "full-stack-bench": "full_stack",
    "error-bench": "error",
}


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Summarize a Styio perf-route artifact directory.")
    parser.add_argument("--run-dir", required=True, help="Artifact directory generated by benchmark/perf-route.sh")
    return parser.parse_args()


def read_metadata(path: Path) -> dict[str, str]:
    rows = read_tsv_rows(path)
    return {row["key"]: row["value"] for row in rows}


def read_tsv_rows(path: Path) -> list[dict[str, str]]:
    if not path.exists():
        return []
    with path.open("r", encoding="utf-8", newline="") as handle:
        reader = csv.DictReader(handle, delimiter="\t")
        return [dict(row) for row in reader]


def coerce_value(raw: str) -> Any:
    if raw == "":
        return raw
    try:
        if raw.startswith(("0", "-0")) and raw not in {"0", "-0"} and "." not in raw:
            raise ValueError
        return int(raw)
    except ValueError:
        try:
            return float(raw)
        except ValueError:
            return raw


def parse_bench_line(line: str) -> dict[str, Any] | None:
    line = line.strip()
    if not line.startswith("[") or "] " not in line:
        return None

    prefix, payload = line[1:].split("] ", 1)
    group = BENCH_PREFIX_TO_GROUP.get(prefix)
    if group is None:
        return None

    record: dict[str, Any] = {"group": group, "kind": prefix}
    for token in payload.split():
        if "=" not in token:
            continue
        key, raw_value = token.split("=", 1)
        record[key] = coerce_value(raw_value)
    return record


def collect_benchmarks(run_dir: Path, sections: list[dict[str, str]]) -> dict[str, list[dict[str, Any]]]:
    grouped: dict[str, list[dict[str, Any]]] = {
        "stage": [],
        "micro": [],
        "full_stack": [],
        "error": [],
    }
    for section in sections:
        log_path = section.get("log_path", "")
        if not log_path:
            continue
        absolute_log = run_dir / log_path
        if not absolute_log.exists():
            continue
        with absolute_log.open("r", encoding="utf-8", errors="replace") as handle:
            for line in handle:
                record = parse_bench_line(line)
                if record is None:
                    continue
                record["section_id"] = section.get("id", "")
                record["log_path"] = log_path
                grouped[record["group"]].append(record)
    return grouped


def write_results_json(path: Path, payload: dict[str, Any]) -> None:
    with path.open("w", encoding="utf-8") as handle:
        json.dump(payload, handle, indent=2, ensure_ascii=False, sort_keys=True)
        handle.write("\n")


def iter_flat_metrics(grouped: dict[str, list[dict[str, Any]]]) -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    metrics_by_group = {
        "stage": [
            "tokenize_us",
            "parse_us",
            "type_us",
            "lower_us",
            "llvm_ir_us",
            "parse_share_pct",
            "lower_share_pct",
            "llvm_ir_share_pct",
            "avg_token_arena_kib",
            "avg_ast_arena_kib",
            "rss_growth_kib",
        ],
        "micro": [
            "focus_us",
            "avg_tokens",
            "avg_token_arena_kib",
            "avg_ast_arena_kib",
            "rss_growth_kib",
        ],
        "full_stack": [
            "cli_wall_us",
        ],
        "error": [
            "error_us",
            "exit_code",
            "avg_diag_bytes",
        ],
    }

    for group, records in grouped.items():
        for record in records:
            name = str(record.get("name", record.get("label", "")))
            for metric in metrics_by_group.get(group, []):
                if metric not in record:
                    continue
                rows.append(
                    {
                        "group": group,
                        "name": name,
                        "module": record.get("module", ""),
                        "label": record.get("label", ""),
                        "metric": metric,
                        "value": record[metric],
                    }
                )
    return rows


def write_benchmarks_csv(path: Path, rows: list[dict[str, Any]]) -> None:
    fieldnames = ["group", "name", "module", "label", "metric", "value"]
    with path.open("w", encoding="utf-8", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)


def markdown_table(headers: list[str], rows: list[list[Any]]) -> str:
    out = []
    out.append("| " + " | ".join(headers) + " |")
    out.append("| " + " | ".join(["---"] * len(headers)) + " |")
    for row in rows:
        out.append("| " + " | ".join(str(cell) for cell in row) + " |")
    return "\n".join(out)


def fmt_num(value: Any, digits: int = 3) -> str:
    if isinstance(value, float):
        return f"{value:.{digits}f}"
    return str(value)


def stage_total_us(record: dict[str, Any]) -> float:
    total = 0.0
    for key in ("tokenize_us", "parse_us", "type_us", "lower_us", "llvm_ir_us"):
        value = record.get(key, 0)
        if isinstance(value, (int, float)):
            total += float(value)
    return total


def write_summary_md(
    path: Path,
    metadata: dict[str, str],
    sections: list[dict[str, str]],
    grouped: dict[str, list[dict[str, Any]]],
) -> None:
    section_rows = [
        [
            row.get("title", ""),
            row.get("status", ""),
            row.get("duration_s", ""),
            row.get("note", ""),
            row.get("log_path", ""),
        ]
        for row in sections
    ]

    stage_rows = []
    for record in sorted(grouped["stage"], key=stage_total_us, reverse=True):
        stage_rows.append(
            [
                record.get("module", ""),
                record.get("label", ""),
                fmt_num(stage_total_us(record)),
                fmt_num(record.get("parse_us", 0)),
                fmt_num(record.get("lower_us", 0)),
                fmt_num(record.get("llvm_ir_us", 0)),
                fmt_num(record.get("rss_growth_kib", 0)),
            ]
        )

    full_stack_rows = []
    for record in sorted(grouped["full_stack"], key=lambda item: float(item.get("cli_wall_us", 0)), reverse=True):
        full_stack_rows.append(
            [
                record.get("module", ""),
                record.get("label", ""),
                fmt_num(record.get("cli_wall_us", 0)),
            ]
        )

    micro_rows = []
    for record in sorted(grouped["micro"], key=lambda item: float(item.get("focus_us", 0)), reverse=True):
        micro_rows.append(
            [
                record.get("focus", ""),
                record.get("name", ""),
                fmt_num(record.get("focus_us", 0)),
                fmt_num(record.get("avg_tokens", 0)),
                fmt_num(record.get("rss_growth_kib", 0)),
            ]
        )

    error_rows = []
    for record in grouped["error"]:
        error_rows.append(
            [
                record.get("category", ""),
                record.get("name", ""),
                fmt_num(record.get("error_us", 0)),
                record.get("exit_code", ""),
                record.get("diagnostic_code", ""),
            ]
        )

    lines: list[str] = []
    lines.append("# Styio Benchmark Summary")
    lines.append("")
    lines.append(f"- Status: `{metadata.get('overall_status', 'unknown')}`")
    lines.append(f"- Started (UTC): `{metadata.get('started_at_utc', '')}`")
    lines.append(f"- Finished (UTC): `{metadata.get('finished_at_utc', '')}`")
    lines.append(f"- Command: `{metadata.get('command', '')}`")
    lines.append(f"- Git: `{metadata.get('git_head', 'unknown')}` on `{metadata.get('git_branch', 'unknown')}` (dirty=`{metadata.get('git_dirty', 'unknown')}`)")
    lines.append(f"- Host: `{metadata.get('hostname', 'unknown')}` / `{metadata.get('cpu_model', 'unknown')}` / `{metadata.get('uname', 'unknown')}`")
    lines.append(f"- Build dir: `{metadata.get('build_dir_abs', metadata.get('build_dir', ''))}`")
    lines.append(
        "- Iterations: "
        f"phase=`{metadata.get('phase_iters', '')}`, "
        f"micro=`{metadata.get('micro_iters', '')}`, "
        f"execute=`{metadata.get('execute_iters', '')}`, "
        f"error=`{metadata.get('error_iters', '')}`"
    )
    lines.append("")

    lines.append("## Route")
    lines.append("")
    lines.append(markdown_table(["Section", "Status", "Duration (s)", "Note", "Log"], section_rows))
    lines.append("")

    lines.append("## Stage Matrix")
    lines.append("")
    if stage_rows:
        lines.append(markdown_table(
            ["Module", "Label", "Total Compile us", "Parse us", "Lower us", "LLVM IR us", "RSS Growth KiB"],
            stage_rows,
        ))
    else:
        lines.append("_No stage benchmark records found._")
    lines.append("")

    lines.append("## Full-Stack Matrix")
    lines.append("")
    if full_stack_rows:
        lines.append(markdown_table(["Module", "Label", "CLI Wall us"], full_stack_rows))
    else:
        lines.append("_No full-stack benchmark records found._")
    lines.append("")

    lines.append("## Micro Benchmarks")
    lines.append("")
    if micro_rows:
        lines.append(markdown_table(["Focus", "Name", "Focus us", "Avg Tokens", "RSS Growth KiB"], micro_rows))
    else:
        lines.append("_No micro benchmark records found._")
    lines.append("")

    lines.append("## Error-Path Benchmarks")
    lines.append("")
    if error_rows:
        lines.append(markdown_table(["Category", "Name", "Error us", "Exit Code", "Diagnostic Code"], error_rows))
    else:
        lines.append("_No error-path benchmark records found._")
    lines.append("")

    with path.open("w", encoding="utf-8") as handle:
        handle.write("\n".join(lines).rstrip() + "\n")


def main() -> int:
    args = parse_args()
    run_dir = Path(args.run_dir).expanduser().resolve()

    metadata = read_metadata(run_dir / "metadata.tsv")
    sections = read_tsv_rows(run_dir / "sections.tsv")
    grouped = collect_benchmarks(run_dir, sections)

    payload = {
        "metadata": metadata,
        "sections": sections,
        "benchmarks": grouped,
    }
    write_results_json(run_dir / "results.json", payload)
    write_benchmarks_csv(run_dir / "benchmarks.csv", iter_flat_metrics(grouped))
    write_summary_md(run_dir / "summary.md", metadata, sections, grouped)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
